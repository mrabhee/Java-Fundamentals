/**
 * Forloop
 */
public class Forloop {
    public static void main(String[] args) {
        int i;
        for(i=1; i<=9; i++){
            if (i%3==0) {
                System.out.println(i+" ");
            }
            else{
                System.out.print(i+" ");
            }
        }
    }
    
}